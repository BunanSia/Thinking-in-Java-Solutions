package ch05ex16;

public class Ch05ex16 {
	
    public static void main(String[] args) {
        String[] arr = new String[]{"abc","def","ghi",};
        for(int i=0; i<arr.length; i++){
            System.out.println("arr["+i+"] = " + arr[i]);
        }
    } 
}

